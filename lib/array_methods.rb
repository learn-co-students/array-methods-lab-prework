def using_include(array, element)
  array.include?(element)
end

def using_sort(array)
  array.sort
end

def using_reverse(array)
  rev=array.reverse
  return rev
end

def using_first(array)
  first=array[0]
  return first
end

def using_last(array)
  last=array.last
  return last 
end

def using_size(array)
  size=array.size
  return size
end
